
package checkpoint.mangaer;

class Entrant {
    private String name;
    private char course;
    private int id;

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the course
     */
    public char getCourse() {
        return course;
    }

    /**
     * @param course the course to set
     */
    public void setCourse(char course) {
        this.course = course;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }
}
